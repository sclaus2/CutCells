// ---------------------------------------------------------------------------
// Thin lookup interface over Basix-generated quadrature tables.
// Generated headers live in cpp/src/generated/quadrature_tables_<cell>.h and
// are produced by tablegen/scripts/gen_quadrature_tables.py.
